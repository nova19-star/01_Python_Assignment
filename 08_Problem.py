# Write a python program to enter the length and breath of the rectangle and display perimeter and area


length = float(input("Enter the length of the rectangle (in meters): "))
breadth = float(input("Enter the breadth of the rectangle (in meters): "))
perimeter = 2 * (length + breadth)
area = length * breadth
print("Perimeter of the rectangle:", perimeter, "meters")
print("Area of the rectangle:", area, "meter ssquare")
